NATURE_THEMES = [
    "forest mist",
    "pine forest fog",
    "alpine lake mountains",
    "ocean cliff waves",
    "surf beach sunrise",
    "glacier valley",
    "desert canyon dawn",
    "northern lights forest",
    "waterfall jungle",
    "mountain road clouds",
]


def is_valid_nature_theme(theme: str) -> bool:
    return theme in NATURE_THEMES